class ResNeXt(nn.Module):
    def __init__(self, name):
        super().__init__()
        if name == 'resnet50':
            model = resnet50(ResNet50_Weights.DEFAULT)
        
        elif name == 'resnet101':
            model = resnet101(ResNet101_Weights.DEFAULT)
            
        elif name == 'resneXt50':
            model = resnext50_32x4d(ResNeXt50_32X4D_Weights.DEFAULT)
        
        elif name == 'resneXt101':
            model = resnext101_32x8d(ResNeXt101_32X8D_Weights.DEFAULT)
        
        else:
            raise ValueError('Invalid backbone name')
        
        self.conv1 = model.conv1
        self.bn1 = model.bn1
        self.relu = model.relu
        self.maxpool = model.maxpool
        
        self.layer1 = model.layer1
        self.layer2 = model.layer2
        self.layer3 = model.layer3
        self.layer4 = model.layer4
        
        self._init_parameters()
        
    def _init_parameters(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                # nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                nn.init.normal_(m.weight, 0, 0.01)
                nn.init.constant_(m.bias, 0)

    def forward(self, x):
        x = self.relu(self.bn1(self.conv1(x)))
        x = self.maxpool(x)
        
        x = self.layer1(x)
        x = self.layer2(x)
        local_featmap = self.layer3(x)
        global_featmap = self.layer4(local_featmap)

        return local_featmap, global_featmap 
    
    
